package com.example.skinJourney

import android.os.Bundle
import android.util.Log
import android.view.LayoutInflater
import android.view.View
import android.view.ViewGroup
import androidx.fragment.app.Fragment
import androidx.navigation.Navigation
import androidx.navigation.fragment.findNavController
import com.example.skinJourney.databinding.FragmentLoginBinding
import com.example.skinJourney.model.FirebaseModel

class LoginFragment : Fragment() {

    private var _binding: FragmentLoginBinding? = null
    private val binding get() = _binding!!

    override fun onCreateView(
        inflater: LayoutInflater, container: ViewGroup?,
        savedInstanceState: Bundle?
    ): View {
        _binding = FragmentLoginBinding.inflate(inflater, container, false)
        return binding.root
    }

    override fun onViewCreated(view: View, savedInstanceState: Bundle?) {
        super.onViewCreated(view, savedInstanceState)

        // Handle login button click
        binding.loginButton.setOnClickListener {
            val firebaseModel = FirebaseModel()

            firebaseModel.register("user@example.com", "password123", "CoolNickname") { success, error ->
                if (success) {
                    Log.d("AUTH", "Registration successful with nickname")
                } else {
                    Log.e("AUTH", "Registration failed: $error")
                }
            }        }

        // Handle navigation to RegisterFragment
        binding.switchToRegister.setOnClickListener {
            Navigation.findNavController(view).navigate(R.id.action_loginFragment_to_registerFragment)
        }
    }

    override fun onDestroyView() {
        super.onDestroyView()
        _binding = null
    }
}
